void ShowIMG(const uchar* ptr, uchar h, uchar w)
{
	uchar i, x, y;
	int count = 0;
	w /= 8;
 
	Send(0, 0x34);        //打开扩展指令集
	//Send(0,0x34);        //打开扩展指令集
	for (i = 0; i < 2; i++)   //上下屏写入
	{
		for (y = 0; y < 32; y++)   //垂直Y写32次
		{
			Send(0, 0x80 + y);       //行地址
			Send(0, 0x80 + i * 8);   //列地址
 
			for (x = 0; x < 16; x++)   //横向X写8次
			{
				if (x < w && count < h)
					Send(1, *ptr++);     //写字节数据
				else
					Send(1, 0x00);     //写字节数据
			}
			count++;
		}
	}
 
	Send(0, 0x36);               //打开绘图显示
	Send(0, 0x30);               //回到基本指令集
}
 
 
