/*******************************************
函数名称：Draw_TX
功    能：在液晶上描绘一个16*16的图形
参    数：Yaddr--Y地址，
		  Xaddr--X地址
		  dp--指向保存图形数据的指针
返回值  ：无
********************************************/
void Draw_TX(uchar Yaddr, uchar Xaddr, const uchar* dp)
{
	uchar j;
	uchar k = 0;
 
	Send(0, 0x34);           //使用扩展指令集，关闭绘图显示
	for (j = 0; j < 16; j++)
	{
		Send(0, Yaddr++);     //Y地址  
		Send(0, Xaddr);       //X地址
		Send(1, dp[k++]);     //送两个字节的显示数据 
		Send(1, dp[k++]);
	}
	Send(0, 0x36);		    //打开绘图显示
	Send(0, 0x30);		    //回到基本指令集模式
}
