//LCD 横向地址8个  每个16位数据
void LCD_ShowChar(char x, char y, char ch1, char ch2)
{
	char i, j;
	char c = 0;
	//合成一个16X16字符的模
	Str2IN1(ch1, ch2);
 
	for (i = 0; i < 16; i++)
	{
		if (y + i >= 32)
		{
			y = y + i - 32;
			break;
		}
		Send(0, 0x80 + y + i);   //行地址
		Send(0, 0x80 + x);       //列地址   一行8个16位
		Send(1, ONE_CHAR[c++]);           //写数据
		Send(1, ONE_CHAR[c++]);           //写数据
	}
	for (j = 0; j + i < 16; j++)
	{
		Send(0, 0x80 + y + j);   //行地址
		Send(0, 0x88 + x);       //列地址   一行8个16位
		Send(1, ONE_CHAR[c++]);           //写数据
		Send(1, ONE_CHAR[c++]);           //写数据
	}
}
 
